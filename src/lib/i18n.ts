// ── Translator ───────────────────────────────────────────
export function getTranslator(lang: Language) {
  return function t(key: string): string {
    const translation = (translations as Record<string, { EN: string; VI: string }>)[key]?.[lang];
    if (translation !== undefined) {
      return translation;
    }
    // Safe user-friendly fallback for missing translation keys:
    // Strip prefixes like auth_, nav_, billing_, history_, checkout_, etc., and replace underscores with spaces.
    const cleanKey = key
      .replace(/^(auth|nav|billing|gen|checkout|history|wig|plan|pricing|revision|diff|results|toast|fmt|tone)_/, '')
      .replace(/_/g, ' ');
    // Capitalize first letter of fallback key
    return cleanKey.charAt(0).toUpperCase() + cleanKey.slice(1);
  };
}

export default translations;
